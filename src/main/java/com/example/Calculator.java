package com.example;

public class Calculator {

    public int sum(int a, int b) {
        return a + b;
    }

    public int diff(int a, int b) {
        return a - b;
    }

    public int product(int a, int b) {
        return a * b;
    }

    public double average(int a, int b) {
        return (a + b) / 2.0;
    }
}
